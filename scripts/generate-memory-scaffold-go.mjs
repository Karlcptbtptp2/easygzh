#!/usr/bin/env node

// Generate the Go source embedded by the standalone easygzh binary. The
// human-edited source of truth remains memory-scaffold/.

import { readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { dirname, join, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const root = resolve(here, "..");
const scaffold = join(root, "memory-scaffold");
const output = join(root, "internal", "memory", "scaffold_generated.go");

function collect(dir) {
  const files = [];
  for (const name of readdirSync(dir).sort()) {
    if (name === ".DS_Store") continue;
    const path = join(dir, name);
    if (statSync(path).isDirectory()) files.push(...collect(path));
    else files.push(path);
  }
  return files;
}

const entries = collect(scaffold).map((path) => {
  const key = relative(scaffold, path).split("\\").join("/");
  const value = readFileSync(path, "utf8");
  return `\t${JSON.stringify(key)}: ${JSON.stringify(value)},`;
});

const generated = `// Code generated by scripts/generate-memory-scaffold-go.mjs; DO NOT EDIT.\n\npackage memory\n\nvar embeddedScaffoldFiles = map[string]string{\n${entries.join("\n")}\n}\n`;
writeFileSync(output, generated);
console.log(`generated ${relative(root, output)} with ${entries.length} files`);
